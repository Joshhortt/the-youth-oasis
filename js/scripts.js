$(document).ready(function() {
  $("h1").click(function() {
    alert("This is a header.");
  });

  $("h2").click(function() {
    alert("This is a medium header.");
  });

  $("ul").click(function() {
    alert("This is unordered list.");
  });
  $("p").click(function() {
    alert("This is a paragraph.");
  });

  $("blockquote").click(function() {
    alert("This is blockquote.");
  });

  $("img").click(function() {
    alert("This is an image.");
  });
});
